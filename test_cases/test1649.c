
#include <stdio.h>

int main() {
    // Testing integer (int) type
    int x = 10;
    printf("Integer: %d\n", x);

    // Testing character (char) type
    char c = 'A';
    printf("Character: %c\n", c);

    // Testing floating point (float) type
    float f = 3.14f;
    printf("Float: %f\n", f);

    // Testing double precision floating point (double) type
    double d = 2.71828;
    printf("Double: %lf\n", d);
    
    // Testing void type
    printf("Void: No type for this test case.\n");

    return 0;
}
