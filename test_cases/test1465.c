
#include <stdio.h>

int main() {
    char c1 = 'a'; // A simple char
    printf("Value of c1: %c\n", c1);

    char c2 = '\x41'; // Hexadecimal character literal
    printf("Value of c2: %c\n", c2);

    char c3 = '\''; // A single quote
    printf("Value of c3: %c\n", c3);

    char c4 = '\101'; // Octal character literal
    printf("Value of c4: %c\n", c4);

    return 0;
}
