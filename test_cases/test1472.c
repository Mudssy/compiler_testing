
#include <stdio.h>

int main(void) {
    int integer_variable = 10;
    char character_variable = 'a';
    float floating_point_variable = 3.14;
    
    printf("Integer variable: %d\n", integer_variable);
    printf("Character variable: %c\n", character_variable);
    printf("Floating point variable: %f\n", floating_point_variable);
    
    return 0;
}
