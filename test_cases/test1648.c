
#include <stdio.h>

int main() {
    // Integer Data Type
    int num1 = 5;
    printf("Integer: %d\n", num1);
    
    // Floating Point Data Type
    float num2 = 4.7f;
    printf("Float: %f\n", num2);
    
    // Double Precision Data Type
    double num3 = 8.9;
    printf("Double: %lf\n", num3);
    
    // Character Data Type
    char ch = 'A';
    printf("Character: %c\n", ch);
    
    return 0;
}
