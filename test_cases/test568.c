
#include <stdio.h>
int main() {
    int num1 = 10;
    int num2 = 5;

    printf("Addition: %d\n", num1 + num2);
    printf("Subtraction: %d\n", num1 - num2);
    printf("Multiplication: %d\n", num1 * num2);
    printf("Division: %f\n", (float)num1 / num2);
    printf("Modulus: %d\n", num1 % num2);

    return 0;
}
