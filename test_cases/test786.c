
#include <stdio.h>

int main() {
    int a = 10;
    int b = 5;
    
    printf("Addition: %d\n", a + b); // should be 15
    printf("Subtraction: %d\n", a - b); // should be 5
    printf("Multiplication: %d\n", a * b); // should be 50
    printf("Division: %d\n", a / b); // should be 2
    printf("Modulus: %d\n", a % b); // should be 0

    return 0;
}
