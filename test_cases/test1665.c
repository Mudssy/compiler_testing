
#include <stdio.h>

int main() {
    int a = 5;        // Assignment operator
    printf("%d\n", a);
    
    a += 2;          // Addition assignment operator
    printf("%d\n", a);
    
    a -= 1;          // Subtraction assignment operator
    printf("%d\n", a);
    
    a *= 3;          // Multiplication assignment operator
    printf("%d\n", a);
    
    a /= 6;          // Division assignment operator
    printf("%d\n", a);
    
    a %= 2;          // Modulus assignment operator
    printf("%d\n", a);
    
    return 0;
}
